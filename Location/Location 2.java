
public class Location
{
	
	double row;
	double column;
	double maxValue;
	
	//Constructor
	public Location()
	{
		row = 0;
		column = 0;
		 maxValue = 0;
	}
	
	//Overloaded Constructor
	public Location(double r, double c, double mv)
	{
		row = r;
		column = c;
		maxValue = mv;
	}
	
	
}
